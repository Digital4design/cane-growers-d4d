            <div class="advocacy-info">
                <p>The campaign was triggered in April 2014 by milling company Wilmar Sugar deciding, without any recourse to its grower suppliers, that it would end its sugar marketing arrangement with the industry-owned not-for-profit marketing company QSL from the end of the 2016 season.</p>
                <p>Tully Sugar and MSF Sugar also took that decision meaning new arrangements would be required before the commencement of the 2017 crush.</p>
                <ul>
                    <li>Growers were immediately alarmed and outraged as the decision meant:</li>
                    <li>Growers supplying those companies would have no say in how their economic interest sugar is taken to market and would be forced to use the milling company's marketing pathway. Grower earnings are directly linked to the net returns from the sale of grower economic interest sugar.</li>
                    <li>Millers would extend their regional milling monopoly power to include marketing.</li>
                </ul>
                <p>CANEGROWERS members rallied in big numbers, wrote letters and sent delegations to see political and business leaders in Brisbane and Canberra demanding the right to choose between the millers proposal and QSL fearing for their future profitability.</p>
                <p>Family farmers faced being placed in a situation where one company had complete control and could sell sugar to its own overseas partners or subsidiaries at a price below what could be achieved by an independent trader in the open world market.</p>
                <p>If allowed to proceed unchallenged, this change to marketing arrangements would have allowed the mills to unilaterally put their only other competition out of business, leaving the way clear for mills to set up their own regional marketing monopoly in its place, with no competition.<br> In December 2015, amendments to the Sugar Industry Act 1999 were passed by a majority of members of the Queensland Parliament requiring mills to offer marketing choice to growers and therefore provided for competition in the provision of sugar marketing services.</p>
                <ul>
                    <li>Two agreements are required to make this happen:</li>
                    <li>A Cane Supply Agreement (CSA) between the growers and a mill for the supply of cane to the mill; and</li>
                    <li>An On-Supply Agreement (OSA) between the mill and the alternate marketer/s who may be chosen by growers.</li>
                    <li>In 2016, CANEGROWERS welcomed first MSF Sugar and then Tully Sugar finalising these agreements.</li>
                </ul>
                <ul>
                    <li>As negotiations involving Wilmar Sugar, grower representatives and QSL dragged on and threatened to impact the start of the 2017 harvest, the federal government introduced a mandatory Sugar Industry Code of Conduct which provides for:</li>
                    <li>Arbitration to settle deadlocks in the negotiations of both CSAs and OSAs, and Grower choice in marketing.</li>
                    <li>The involvement of an independent mediator helped to resolve the deadlocks and CANEGROWERS members supplying Wilmar mills signed CSAs just days before the harvest began in early June 2017.</li>
                    <li>You can download the CANEGROWERS Position Statement on Marketing Choice.</li>
                </ul>                        
